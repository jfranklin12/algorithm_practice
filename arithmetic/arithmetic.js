// Fill in the body of each function with the code required

var add = function(num1, num2) {
    let sum = num1 + num2;
    return sum;
};

var subtract = function(num1, num2) {
    let dif = num1 - num2;
    return dif;
};

var multiply = function(num1, num2) {
    let product = num1 * num2;
    return product;
};

var divide = function(num1, num2) {
    let quo = num1 / num2;
    return quo;
};
